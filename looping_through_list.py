seasons = ['winter', 'spring', 'summer', 'autumn']
for season in seasons:
    # this is an inside of the loop, executed once for each element in the loop
    print(season.title())
    print(f"{season.title()} is a great season!")
    print(f"Welcome to the {season} season.\n")
print("Fall is also a season!")

